<P>
	This project, developed for the Kiel University in collaboration with multimedia designer
	<A href="https://anketornow.de/" target="_blank">Anke Tornow</A>
	, aims to showcase the intricate connections between human societies, environments, and
	cultures over thousands of years. Our team has worked hard to create a portal that both experts
	and the general public can explore with ease.
</P>

<P>
	ROOTS Compass provides a unique entry point into the research, allowing users to navigate a
	virtual globe to explore projects spread across continents, from ancient Pompeii to medieval North
	Rhine-Westphalia. By integrating various filters—such as historical periods, scientific
	disciplines, and research methods—users can uncover fascinating interdisciplinary links across
	different projects. Whether you're interested in archaeological excavations, DNA analysis, or
	computer modeling, the portal reveals the rich diversity of approaches used in the research at the
	ROOTS cluster.
</P>

<P>
	For me, as the software developer for ROOTS Compass, this project has been a rewarding challenge.
	Creating an interactive frontend that integrates geo data visualization with highly connected data
	structures was both exciting and complex. Balancing user-friendly design with the dynamic
	exploration of connected information pushed us to innovate, especially in handling intricate
	project relationships and real-time map performance.
</P>

<P>
	I'm proud to be part of this project, which not only makes cutting-edge research more accessible
	but also invites users to explore and engage with the complexities of our shared past. You can
	experience it yourself by visiting the portal at

	<A target="_blank" href="https://www.roots-compass.org">www.roots-compass.org</A>
	.
</P>

